# Safe fallback when PriceCharting has no CIB data. 1.8x is the middle of the
# 1.5x–3x range typical for retro CIB premiums — safe enough to unstick the
# pricing without creating absurd values.
CIB_UPLIFT_MULTIPLIER = 1.8

# Never exceed this as a safety belt
MAX_MARKET_PRICE = 800.0

SEARCH_DELAY = 1.8
SHOPIFY_DELAY = 0.25


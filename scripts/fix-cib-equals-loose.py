#!/usr/bin/env python3
"""
Fix products where the CIB variant price equals the Loose (Game Only) price.

Background: In 2026-04, while preparing for Google Ads launch, a scan found
~22% of multi-variant retro game products (1,364 of 6,112) had their CIB
variant priced identically to their Loose variant. Root cause: earlier
`search-price-refresh.py` runs couldn't find these products on PriceCharting
(low match rate on obscure titles), so CIB stayed at whatever value it was
seeded with — which was the loose price from when the variant was created.

This script is *targeted* — it only touches products that are currently
CIB==Loose, not the whole catalog. For each such product, it:

  1. Searches PriceCharting by "{title} {console}"
  2. If a result has cib_market > 0 AND cib_market > loose_market,
     recalculates both prices and updates both variants.
  3. Falls back to a multiplicative CIB uplift (1.8x loose) if PriceCharting
     has no CIB data — retro CIB typically sells 1.5x–3x loose, so 1.8x is
     a safe floor that unsticks the pricing without overshooting.
  4. Skips anything where the loose market is below $3 (fee-floor) or the
     computed CIB sell price would change by less than $1.

Input: data/cib-equals-loose.json (generated by the fresh catalog scan)
Output: data/logs/fix-cib-equals-loose-{timestamp}.{csv,log}

Usage:
    python3 scripts/fix-cib-equals-loose.py                 # Dry run (report only)
    python3 scripts/fix-cib-equals-loose.py --apply         # Actually update Shopify
    python3 scripts/fix-cib-equals-loose.py --apply --uplift-only   # Skip PC search, just apply 1.8x uplift
"""
